fun main(){
    //Data types in Kotlin

    //Numbers
    val intValue = 1 //Integer
    val longValue1 = 200000 //Long
    val longValue2 = 1L //Long
    val byteValue : Byte = 1 //Byte
    val doubleValue = 1.0  //double
    val floatValue = 1.2345678  //float


    //Boolean

    /*Boolean - Either TRUE or FALSE can be stored
    in a boolean data type variable*/

    val trueValue = true //TRUE
    val falseValue = false //FALSE


    //Characters
    val characterValue="A"

    //Strings
    val stringValue = "ABCDEFGH"

// Print all variables
    println("intValue: $intValue")
    println("longValue1: $longValue1")
    println("longValue2: $longValue2")
    println("byteValue: $byteValue")
    println("doubleValue: $doubleValue")
    println("floatValue: $floatValue")
    println("trueValue: $trueValue")
    println("falseValue: $falseValue")
    println("characterValue: $characterValue")
    println("stringValue: $stringValue")
}
